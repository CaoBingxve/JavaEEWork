package com.itheima;

public class MyClass {  
    private String name;  
    private int age;

    public MyClass(String name, int age) {  
        this.name = name;  
        this.age = age;  
    }

    public void show() {  
        System.out.println("My Name is  " + name + ", Age is " + age);  
    }  
}  
